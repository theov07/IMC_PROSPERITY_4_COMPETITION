class OracleDay2ReplayStrategy(BaseStrategy):
    """Replay the exact DP-optimal taker schedule for day2 0..99900."""

    def compute_orders(
        self,
        state: TradingState,
        book: BookSnapshot,
        order_depth: OrderDepth,
        position: int,
        memory: Dict[str, Any],
    ) -> Tuple[List[Order], int]:
        action = ORACLE_SCHEDULE.get(self.product, {}).get(int(state.timestamp))
        if action is None:
            return [], 0
        side, qty, price = action
        if side == "BUY":
            qty = min(int(qty), self.buy_capacity(position))
            if qty <= 0:
                return [], 0
            return [Order(self.product, int(price), qty)], 0
        qty = min(int(qty), self.sell_capacity(position))
        if qty <= 0:
            return [], 0
        return [Order(self.product, int(price), -qty)], 0

    def feature_prices(self, memory: Dict[str, Any]) -> Dict[str, float]:
        return {}

